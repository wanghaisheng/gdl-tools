package se.cambio.cds.gdl.model;

import java.io.Serializable;

public class ElementBinding  implements Serializable{

	/**
     * 
     */
    private static final long serialVersionUID = 1L;
	public ElementBinding() {
	}

	/**
	 * Create an Element with id, path
	 * 
	 * @param id
	 * @param path
	 */
	public ElementBinding(String id, String path) {
		if (id == null || id.isEmpty()) {
			throw new IllegalArgumentException("Invalid id");
		}
		if (path == null || path.isEmpty()) {
			throw new IllegalArgumentException("Invalid path");
		}
		this.id = id;
		this.path = path;
	}

	/**
	 * @return the id as a gtcode in the guide
	 */
	public String getId() {
		return id;
	}

	/**
	 * @return the path
	 */
	public String getPath() {
		return path;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @param path
	 *            the path to set
	 */
	public void setPath(String path) {
		this.path = path;
	}

	private String id;
	private String path;
	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((path == null) ? 0 : path.hashCode());
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ElementBinding other = (ElementBinding) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (path == null) {
			if (other.path != null)
				return false;
		} else if (!path.equals(other.path))
			return false;
		return true;
	}
}
